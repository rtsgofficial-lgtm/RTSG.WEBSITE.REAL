export function findShopProduct(productId: string | null | undefined) {
  if (!productId) {
    return null;
  }

  return SHOP_PRODUCTS.find((product) => product.id === productId) ?? null;
}

export function findShopVariant(variantId: string | null | undefined, productId?: string | null) {
  if (!variantId) {
    return null;
  }

  const products = productId ? SHOP_PRODUCTS.filter((product) => product.id === productId) : SHOP_PRODUCTS;

  for (const product of products) {
    const variant = product.variants.find((item) => item.id === variantId);

    if (variant) {
      return variant;
    }
  }

  return null;
}
